    private String invertir(String texto){
       
        Stack<Character> pila = new Stack<>();
        for (char c : texto.toCharArray()) {
            pila.push(c);
        }

        StringBuilder sb = new StringBuilder();
        while (!pila.isEmpty()) {
            sb.append(pila.pop());
        }

        return sb.toString();
        
    }
