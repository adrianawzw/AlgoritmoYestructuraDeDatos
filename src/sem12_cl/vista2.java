    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nom = txt_nom.getText();
        String pel = txt_pelicula.getText();
      
        if (!nom.isEmpty() || !pel.isEmpty()) {
            Persona pers = new Persona(nom, pel);
            boolean agregado = cola.offer(pers);
            if (agregado){ 
                txt_area.setText("Cliente agregado a cola");
                txt_nom.setText("");
                txt_pelicula.setText("");
            }else txt_area.setText("Cliente no se pudo agregar a cola");
        }else{
            txt_area.setText("Completar todos los campos!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            cola.remove();
            txt_area.setText("Cliente atendido");
        } catch (NoSuchElementException e) {
            txt_area.setText("Error: No se puede remover de una cola vacia");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        for(Persona p: cola){
            sb.append(p.getNombre()).append(" - ").append(p.getPelicula()).append("\n");
        }
        txt_area.setText(sb.toString());
    }//GEN-LAST:event_jButton3ActionPerformed

