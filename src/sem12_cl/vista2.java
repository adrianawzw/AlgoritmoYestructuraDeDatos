    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String filtro = txt_fil.getText();
        if (!filtro.isEmpty()) {
            txt_area.setText("Personas "+filtrarPorPelicula(filtro)+" en pelicula");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    public int filtrarPorPelicula(String pelicula) {
        int contador = 0;
        for (Persona persona : cola) {
            if (persona.getPelicula().equalsIgnoreCase(pelicula)) {
                contador++;
            }
        }
        return contador;
    }
