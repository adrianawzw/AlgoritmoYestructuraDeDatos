    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        String nombre = txt_nombre.getText();
        int cod = Integer.parseInt(txt_codigo.getText());
        int stock = Integer.parseInt(txt_stock.getText());
        
        lista.agregar(cod, nombre, stock);
        limpiar();
        JOptionPane.showMessageDialog(this, "Producto codigo "+cod+" agregado al inicio!");
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int cod = Integer.parseInt(txt_codeliminar.getText());
        
        lista.eliminar(cod);
        txt_codeliminar.setText("");
        JOptionPane.showMessageDialog(this, "Producto codigo "+cod+" eliminado!");
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrarActionPerformed
        // TODO add your handling code here:
        String mos = lista.mostrar();
        
        txt_res.setText(mos);
    }//GEN-LAST:event_btn_mostrarActionPerformed

    private void limpiar(){
        txt_codigo.setText("");
        txt_nombre.setText("");
        txt_stock.setText("");
    }
