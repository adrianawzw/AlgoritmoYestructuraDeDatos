    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nom = txt_nom.getText();
        String ope = String.valueOf(cb_oper.getSelectedItem());
        if (!nom.isEmpty() && !ope.isEmpty()) {
            try {
                cola.add(new Cliente(nom, ope));
                switch(ope){
                    case "Retiro": retiro++; break;
                    case "Deposito": deposito++; break;
                    case "Consulta": consulta++; break;
                    case "Otro": otro++; break;
                    default: System.out.println("Tipo de operacion no soportada");
                }
                txt_res.setText("Cliente agregado correctamente");
                txt_nom.setText("");
                cb_oper.setSelectedItem(null);
            } catch (IllegalStateException e) {
                txt_res.setText("No se pudo agregar");
            }
        } else {
            txt_res.setText("Completa ambos campos antes de agregar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

