    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nom = txt_nom.getText();
        String mot = String.valueOf(cb_motivo.getSelectedItem());
        if (!nom.isEmpty() && !mot.isEmpty()) {
            try {
                cola.add(new Paciente(nom, mot)); 
                txt_res.setText("Paciente agregado correctamente");
                txt_nom.setText("");
                cb_motivo.setSelectedItem(null);
            } catch (IllegalStateException e) {
                txt_res.setText("No se pudo agregar");
            }
        } else {
            txt_res.setText("Completa ambos campos antes de agregar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Paciente elim = cola.remove();
            txt_res.setText("Atendiendo y eliminando a: "+elim.toString()+"");
        }catch(NoSuchElementException e){
            txt_res.setText("La cola esta vacia");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        for(Paciente p: cola){
            sb.append(p.toString()).append("\n");
        }
        txt_res.setText(sb.toString());
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int tam = cola.size();
        txt_res.setText("Cantidad de pacientes en cola "+tam);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String filtro = txt_filtro.getText();
        int cont=0;
        for(Paciente p: cola){
            if(p.motivo.equals(filtro)){
                cont++;
            }
        }
        txt_res.setText("Cantidad de personas por: "+filtro+" "+cont);
    }//GEN-LAST:event_jButton5ActionPerformed

