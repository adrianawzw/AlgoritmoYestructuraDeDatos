    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nom = txt_nom.getText();
        String dur = txt_dur.getText();
        
        if (!nom.isEmpty() || !dur.isEmpty()) {
            if (!nom.equals("Juan")) {
                pila.push(new Llamada(nom, Integer.parseInt(dur)));
            } else {
                txt_area.setText("Llamada descartada");
            }
            txt_nom.setText("");
            txt_dur.setText("");
        } else {
            txt_area.setText("Completar los campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!pila.isEmpty()) {
            Llamada elim = pila.pop();
            txt_area.setText("Llamada de: " + elim.getNombre() + " atendida");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        for(Llamada p: pila){
            sb.append(p.getNombre()).append(" duración: ").append(p.getDuracion()).append("\n");
        }
        txt_area.setText(sb.toString());
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        //ArrayList<Llamada> array = new ArrayList<>(pila);
        Llamada maslarg=null;
        for(Llamada p: pila){
            if (maslarg==null || p.getDuracion()>maslarg.getDuracion()) {
                maslarg = p; 
            }
        }
        
        if (maslarg != null) {
        txt_area.setText("Llamada más larga: " + maslarg.getDuracion());
    } else {
        txt_area.setText("No hay llamadas en la pila.");
    }
    }//GEN-LAST:event_jButton4ActionPerformed

