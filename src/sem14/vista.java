    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nom = txt_nom.getText();
        String gen = String.valueOf(cb_gen.getSelectedItem());
        boolean esHombre = false;
        if (gen.equals("Hombre")) {
            esHombre = true;
        }
        
        if(!nom.isEmpty() || !gen.isEmpty()){
            arb.agregarMiembro(nom, esHombre);
            txt_nom.setText("");
            cb_gen.setSelectedItem("null");
            txt_res.setText("Miembro agregado");
        }else{
            txt_res.setText("Completar campos");
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

